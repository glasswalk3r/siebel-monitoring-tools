=head1 Name

Nagios::Plugin::Siebel::Srvrmgr - Perl module to enable interaction between Siebel::Srvrmgr and Nagios as a plugin

=head1 Description

This module is only Pod regarding the distribution.

The distribution Nagios-Plugin-Siebel-Srvrmgr has several modules used to integrate L<Siebel::Srvrmgr> API with Nagios by build plugins.

This release includes implementations made as an XML-RPC server and an XML-RPC client, being the last the real Nagios plugin.

